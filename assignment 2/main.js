"use strict";
// Storing the person name in a variable
let personName = "Sami Gulhasan";
// Printing a person name to the Lowercase
console.log("Lowercase:", personName.toLowerCase());
// Printing a person name to the Uppercase
console.log("Uppercase:", personName.toUpperCase());
// Printing a person name to the Titlecase
console.log("Titlecase:", personName.totitlecase());
